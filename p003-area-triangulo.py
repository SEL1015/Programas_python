#Calcular el area del triangulo

print("Calcular el area de un triangulo:\n")
print ("Dame la base y altura separados por un Enter")

#Entrada
base,altura=float(input()),float(input())
area= base*altura/2


#Salida cla
print(f"el triangulo de base {base:.2f}y altura {altura:.2f} tiene un area de {area:.2f}")
