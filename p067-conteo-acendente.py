# Imprime números del 1 al 100 usando un ciclo for
print("\033[H\033[J")
print(" Iniciando secuencia de conteo ascendente...")
print("Imprimiendo numeros de 1 al 100 con ciclo for:\n ")
for h in range(1, 101, 1):
    print(h, end=' ')#Salto de linea horizontal con espacio 